    {
        $cliente = new Cliente();

        $cliente -> nombre = $request['nombre'];
        $cliente -> numero_celular = $request['numero_celular'];
        $cliente -> rfc = $request['rfc'];
        $cliente -> correo = $request['correo'];

        $cliente -> save();

        return redirect()->route('empleado.index');
    }

